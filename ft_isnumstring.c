int	ft_isnumstr(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		if (!((str[i] >= '0' && str[i] <= '9')
				|| (str[i] == '.' || str[i] == '+' || str[i] == '-')))
			return (1);
		i++;
	}
	return (0);
}
